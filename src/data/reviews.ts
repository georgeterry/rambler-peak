export const featuredReviews = reviews.slice(0, 3);

export const productReviews = (slug: string) => reviews.filter((r) => r.productSlug === slug);

export const ratingDistribution = (slug?: string) => {
  const set = slug ? reviews.filter((r) => r.productSlug === slug) : reviews;
  const total = set.length || 1;
  return [5, 4, 3, 2, 1].map((star) => {
    const count = set.filter((r) => r.rating === star).length;
    return { star, count, percent: Math.round((count / total) * 100) };
  });
};
